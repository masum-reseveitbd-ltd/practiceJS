var marks = prompt("Enter your marks:");

if (marks >= 80) {
  console.log("You result is A+");
} else if (marks >= 75) {
  console.log("You result is A");
} else if (marks >= 70) {
  console.log("You result is A-");
} else if (marks >= 65) {
  console.log("You result is B+");
} else if (marks >= 60) {
  console.log("You result is B");
} else if (marks >= 50) {
  console.log("You result is B-");
} else if (marks >= 45) {
  console.log("You result is C");
} else {
  console.log("You result is F");
}
